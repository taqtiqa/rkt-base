function json_value() {
  KEY=${1-'key'}
  num=${2-''}
  awk -F"[,:}]" '{for(i=1;i<=NF;i++){if($i~/'$KEY'\042/){print $(i+1)}}}' | tr -d '"' | sed 's/\\n/\n/g' | sed -n ${num}p
}

function travisend() {
    export EXIT=$?
    rm -f ./travis-ca.cert
    exit $EXIT
}

